public class Sample02 {
    public static void main(String[] args) {
        //operadores aritiméticos: + - / * % (resto da divisão)
        System.out.println(2+3*4); // abrev. syso
        System.out.println((2+3)*4); //o ln faz passar para a próxima linha
        System.out.println(21%4);
        System.out.println("A soma de 2+3= " + (2+3)); // com o texto o + faz a função de concatenar e considera o restante como texto também
        System.out.println("Texto em várias \nlinhas\n"); // comando para quebrar a linha é \n

    }
}